package course_registeration_Java;

public class Course {
	private String department = "";
	private String courseNumber = "";
	private String CRN = "";
	
	Course(){
		
	}
	
	String getDepartment() {
		return this.department;
	}
	
	String getCourseNumber() {
		return this.courseNumber;
	}
	
	String getCRN() {
		return this.CRN;
	}
	
}
